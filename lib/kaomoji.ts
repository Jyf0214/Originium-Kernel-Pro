/**
 * 颜文字库：标签页切换时显示的可爱提示语
 *
 * 用户切出标签页时，随机选一条显示在标题栏
 */
const TAB_TITLE_MESSAGES = [
  '再回来看看呀～(◕ᴗ◕✿)',
  '想你了 (｡•́︿•̀｡)',
  '别走嘛～(っ˘̩╭╮˘̩)っ',
  '我在这里等你哦 ♪(´▽`)',
  '不要离开我... (ಥ﹏ಥ)',
  '快回来！(´；ω；`)',
  '偷偷想你中... (◡‿◡✿)',
  '一个人好寂寞 (｡ŏ﹏ŏ)',
  '去哪了嘛 (´・ω・`)',
  '你是不是忘了什么？(゜ロ゜)',
  '我会乖乖等你的 (◕‿◕)♡',
  '还没走远吧？快回来！(ﾉ◕ヮ◕)ﾉ*:・ﾟ',
  '摸鱼被我发现了吧 (∩｀-´)⊃━☆ﾟ.*・',
  '回来陪我玩嘛～(≧▽≦)',
  '趁你不在偷偷变可爱 (●´∀｀●)',
] as const;

/**
 * 随机获取一条标签页标题颜文字
 */
export function getRandomTabTitle(): string {
  const idx = Math.floor(Math.random() * TAB_TITLE_MESSAGES.length);
  return TAB_TITLE_MESSAGES[idx] ?? '再回来看看呀～(◕ᴗ◕✿)';
}
